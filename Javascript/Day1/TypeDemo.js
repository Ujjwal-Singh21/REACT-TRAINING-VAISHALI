// Core JS Objects -> String/Math/Array/Number/Boolean/RegExp/Date/Function/Object 
// JS is interpretd and Object-Based scripting language, it is loosely type language.

var fname = 'Bruce Wayne';
var age = 45;
var salary = 4500.00;
var isEmployed = true;
