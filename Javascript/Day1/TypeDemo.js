console.log('Age: ', age);

console.log('type of salary: ', typeof salary);
console.log('Salary: ', salary);

console.log('type of isEmployed: ', typeof isEmployed);
console.log('IsEmployed: ', isEmployed);

console.log('type of doj: ', typeof doj);
console.log('Date of Joining: ', doj);

console.log('type of companyName: ', typeof companyName);
